                                so students will obtain 360 credits upon successful completion of the three-year course. Participants on the four-year sandwich course will receive 120 practice credits for the additional year.
                                All candidates should meet specific requirements (please check the website www.mmu.ac.uk/international indicating your nationality and study level).
                                For further details please do not hesitate to contact the information office.
                                <br>
                                <br>
                                ** Participants who successfully complete the three year courses ‘Fashion Design & Accessories*’,
                                ‘Interior Design*’, ‘Product Design’, and ‘Visual Design’ in Milano and Firenze, will be awarded the AFAM First Level Academic Diploma.
                                Recognised by the Italian Ministry of Education as an academic diploma equivalent to a university undergraduate level degree,
                                participants will obtain 180 CFA (crediti formativi accademici) equivalent to 180 ECTS credits.
                                <br>
                                The pathways held in Milano and Firenze ‘Fashion Design & Accessories with Womenswear Pathway’,
                                ‘Fashion Design & Accessories with Menswear Pathway’ are a specialisation of the recognised AFAM main courses ‘Fashion Design & Accessories’
                                (Pathway final diplomas will state the main course title).
                                <br>
                                <br>
                                *** Participants who successfully complete one of the following three-year ‘RNCP’ courses taught in Paris will receive the corresponding title of:
                                Fashion Design course – 'Fashion Designer (Styliste-Crèateur/trice)’; Fashion Styling course – ‘Responsable de la communication et de l’image de mode';
                                Fashion Business course – ‘Responsable de la stratégie marketing et commerciale de mode'.
                                As recognised vocational training, level II RNCP is equivalent to a three-year program of higher education with participants obtaining 180 ECTS credits upon
                                successful completion. RNCP is acknowledged by the CNCP Commission Nationale de la Certification Professionnelle
                                (National Commission for Vocational Certification) and registered as a Professional Certificate published in the JORF (Official Journal of the French Republic).
